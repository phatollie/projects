"""

Description: Variable control file for the mensaq game providing a single location for all required game parameters.
"""

def get_min(self):
    """
    Minimum number to for game to start counting from.
    """
    return 1

def get_max(self):
    """
    Maximum number to for game to stop counting.
    """
    return 100

def get_var(self):
    """
    Number to identify/search within the range of number given for game.
    """
    return 7

